
package controller;

public class OperacionComparacion {
    public static void main(String[] args) {
       int a = 127 , b = 39, c = 71;
        System.out.println( a + " > " + b + " es mayor, true or false : " + (a > b));
        System.out.println( a + " < " + b + " es mayor, true or false : " + (a < b));
        System.out.println( a + " != " + c + " es mayor, true or false : " + (a != b));
        
       
    }
}
